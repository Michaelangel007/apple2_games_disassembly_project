zTile = $FF

KEYBOARD  = $C000
KEYSTROBE = $C010

TEXT = $F399
HOME = $FC58
HGR  = $F3E2 ; // We do not use since it is not on the Apple ][; only Apple ][+, etc.
TABV = $FB5B ; STA CV, JMP VTAB
VTAB = $FC22

        ORG $6000

Main   
        LDA #$01        ; Use selected sprite
        PHA
            JSR TEXT
            JSR HOME

            LDA #0
            LDX #$20    ; HGR Page 1
            TAY
            STA zRow
            STX zRow+1
ClearHGR    STA (zRow),Y
            INY
            BNE ClearHGR
            INC zRow+1
            LDX zRow+1
            CPX #$40    ; HGR Page 2
            BNE ClearHGR
            STA $C050  ; Graphics
            STA $C053  ; Mixed
            STA $C054  ; Page 1
            STA $C057  ; HGR

            LDX #$FF     ; **INTENTIONAL** Start at -1
            STX zTile
            LDY #3
            STY zRow
AtlasChar
            INC zTile
            LDA zTile
            CMP #$80      ; Drawn all 128 tiles?
            BEQ AtlasDone
            TAY
                AND #$0F
                BNE SameRow  
                LDA #4      ; Center 16 tiles with $20 width
                STA zCol    ; ($28 - $20)/2 = 4
                LDA zRow
                CLC
                ADC #$10
                STA zRow
SameRow     TYA
            JSR DrawSprite
            INC zCol
            INC zCol
            BNE AtlasChar   ; Always
AtlasDone   LDA #0
            STA zCol
            STA zRow
        PLA
        STA zTile

NextSprite
        LDA zTile
        STA $7D3            ; Display tile number as ASCII character
        PHA
          PHA
            ROR
            ROR
            ROR
            ROR
            LDX #0
            JSR PutChar    ; Display tile number in hexadecimal
          PLA
          JSR PutChar
        PLA
        JSR DrawSprite

Wait    LDA KEYBOARD
        BPL Wait
        STA KEYSTROBE

        LDX zTile
        CMP #$88    ; Ctrl-H <-
        BEQ Prev
        CMP #'J' + $80
        BEQ Prev
        CMP #$95    ; Ctrl-U ->
        BEQ Next
        CMP #'L' + $80
        BNE Done    ; if == fall into Next
Next    INX
        INX
Prev    DEX
        TXA
        AND #$7F
        STA zTile
        JMP NextSprite

Done    STA $C053
        LDA #23
        JMP TABV

PutChar AND #$F
        TAY
        LDA HEX2ASC,y
        STA $7D0,x  ; VTAB 24
        INX
        RTS

DrawSprite
        LDX #>Tiles
        STX ReadEvn+2
        STX ReadOdd+2
        LDX #<Tile_0a
        STX ReadEvn+1
        LDX #<Tile_0b
        STX ReadOdd+1
        TAX             ; Col
        LDY zRow        ; Row
        TYA
        CLC
        ADC #11
        STA LastRow+1   ; ***SELF-MODIFIES***
CopySprite
        LDA HGR_Y_HI,Y
        ORA #$20        ; HGR Page 1 = $2000
        STA SaveEvn+2
        STA SaveOdd+2
        LDA HGR_Y_LO,Y
        CLC
        ADC zCol
        STA SaveEvn+1
        CLC
        ADC #1
        STA SaveOdd+1

ReadEvn LDA Tiles + $000,X
        ORA #$80
SaveEvn STA $2000       ; ***SELF-MODIFIED***
ReadOdd LDA Tiles + $080,X
        ORA #$80
SaveOdd STA $2001       ; **SELF-MODIFIED***
        INC ReadEvn+2
        INC ReadOdd+2

        INY
LastRow CPY #11         ; ***SELF-MODIFIED***
        BNE CopySprite
        RTS

        DS \,0

